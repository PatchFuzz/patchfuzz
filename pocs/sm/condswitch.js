function ret2() {
    return 2;
}


function test0(x) {
    var res = 0;
    switch (x) {
    case (res |= 0x40, 1): 
        res |= 0x1;
    default: 
        res |= 0x2;
    case (res |= 0x80, ret2()): 
        res |= 0x4;
    case (res |= 0x100, 1 + ret2()): 
        res |= 0x8;
        break;
    case (res |= 0x200, 0): 
        res |= 0x10;
    }
    res |= 0x20;
    return res;
}

print(test0(0), 0x40 | 0x80 | 0x100 | 0x200 | 0x10 | 0x20);
print(test0(1), 0x40 | 0x1 | 0x2 | 0x4 | 0x8 | 0x20);
print(test0(2), 0x40 | 0x80 | 0x4 | 0x8 | 0x20);
print(test0(3), 0x40 | 0x80 | 0x100 | 0x8 | 0x20);
print(test0(4), 0x40 | 0x80 | 0x100 | 0x200 | 0x2 | 0x4 | 0x8 | 0x20);


